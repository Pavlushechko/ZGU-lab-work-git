# Программа выводит "Hello, World!" и имеет несколько функций для работы с числами.

# Вывод "Hello!"
print("Hello from feature 2 for main with love)!")

# Функция для сложения двух чисел

def add_numbers_a_b(a, b):
    return a + b

# Функция для умножения двух чисел
def multiply_numbers(a, b):
    return a * b

# Новая функция для вычитания двух чисел
def subtract_numbers(a, b):
    return a - b

# Тестирование всех функций
print(add_numbers_a_b(5, 3))        # Ожидаемый результат: 8
print(multiply_numbers(5, 3))   # Ожидаемый результат: 15
print(subtract_numbers(5, 3))   # Ожидаемый результат: 2

